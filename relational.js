// Relational Operators

{/*
< less than
> greater than
<= less than equal to
>= greater than equal to
== equal equals to 
!= not equals to
*/}

let a=10,b=20,c=10;

console.log(a<b);
// true
console.log(a>b);
// false
console.log(a<=b);
// 10<=20
// true
console.log(a>=b);
// 10>=20
// false
console.log(a>=c);
// 10>=10
// true
console.log(a==b);
// 10==20
// false
console.log(a==c);
// true
console.log(a!=b);
// 10!=20
// true
console.log(a!=c);
// 10!=10
// false


